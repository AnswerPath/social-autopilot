interface EmojiPickerProps {
  isOpen: boolean
  onClose: () => void
  onEmojiSelect: (emoji: string) => void
  className?: string
}

export function EmojiPicker({ isOpen, onClose, onEmojiSelect, className }: EmojiPickerProps) {
  const [searchQuery, setSearchQuery] = useState('')
  const [selectedCategory, setSelectedCategory] = useState<keyof typeof EMOJI_CATEGORIES>('people')
  const [recentEmojis, setRecentEmojis] = useState<string[]>([])

  // Load recent emojis from localStorage
  useEffect(() => {
    const saved = localStorage.getItem('recent-emojis')
    if (saved) {
      const parsed = JSON.parse(saved)
      setRecentEmojis(parsed)
      EMOJI_CATEGORIES.recent.emojis = parsed
      if (parsed.length > 0) {
        setSelectedCategory('recent')
      }
    }
  }, [])

  // Filter emojis based on search query
  const filteredEmojis = useMemo(() => {
    if (!searchQuery.trim()) {
      return EMOJI_CATEGORIES[selectedCategory].emojis
    }

    const query = searchQuery.toLowerCase()
    const keywordMatches = KEYWORD_TO_EMOJIS[query] ?? []
    const allEmojis = Object.values(EMOJI_CATEGORIES).flatMap(category => category.emojis)
    const directMatches = allEmojis.filter(emoji => emoji.toLowerCase().includes(query))

    return [...new Set([...keywordMatches, ...directMatches])]
  }, [searchQuery, selectedCategory])

  // Handle emoji selection
  const handleEmojiSelect = (emoji: string) => {
    // Add to recent emojis
    const newRecent = [emoji, ...recentEmojis.filter(e => e !== emoji)].slice(0, 20)
    setRecentEmojis(newRecent)
    localStorage.setItem('recent-emojis', JSON.stringify(newRecent))
    EMOJI_CATEGORIES.recent.emojis = newRecent

    // Call parent handler
    onEmojiSelect(emoji)
    onClose()
  }

  // Handle search
  const handleSearch = (query: string) => {
    setSearchQuery(query)
    if (query.trim()) {
      setSelectedCategory('recent') // Show search results in recent category
    }
  }

  return (
    <Dialog open={isOpen} onOpenChange={onClose}>
      <DialogContent className={cn("max-w-md p-0", className)}>
        <DialogHeader className="px-6 pt-6 pb-2">
          <DialogTitle className="text-lg font-semibold">Choose an emoji</DialogTitle>
        </DialogHeader>
        
        <div className="px-6 pb-6 space-y-4">
          {/* Search */}
          <div className="relative">
            <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 h-4 w-4 text-gray-400" />
            <Input
              placeholder="Search emojis..."
              value={searchQuery}
              onChange={(e) => handleSearch(e.target.value)}
              className="pl-10"
              onKeyDown={(event) => {
                if (event.key === 'Escape') {
                  event.preventDefault()
                  onClose()
                }
              }}
            />
          </div>

          {/* Category tabs */}
          {!searchQuery.trim() && (
            <div className="flex flex-wrap gap-1">
              {Object.entries(EMOJI_CATEGORIES).map(([key, category]) => {
                const Icon = category.icon
                return (
                  <Button
                    key={key}
                    variant={selectedCategory === key ? "default" : "ghost"}
                    size="sm"
                    onClick={() => setSelectedCategory(key as keyof typeof EMOJI_CATEGORIES)}
                    className="h-8 px-2"
                    title={category.name}
                  >
                    <Icon className="h-4 w-4" />
                    {selectedCategory === key && (
                      <span className="ml-1 text-xs">{category.name}</span>
                    )}
                  </Button>
                )
              })}
            </div>
          )}

          {/* Emoji grid */}
          <div className="grid grid-cols-8 gap-1 max-h-64 overflow-y-auto">
            {(searchQuery.trim() ? filteredEmojis : EMOJI_CATEGORIES[selectedCategory].emojis).map((emoji, index) => (
              <Button
                key={`${emoji}-${index}`}
                variant="ghost"
                size="sm"
                onClick={() => handleEmojiSelect(emoji)}
                className="h-10 w-10 p-0 text-lg hover:bg-gray-100"
                title={emoji}
              >
                {emoji}
              </Button>
            ))}
          </div>

          {/* Empty state */}
          {filteredEmojis.length === 0 && searchQuery.trim() && (
            <div className="text-center py-8 text-gray-500">
              <Smile className="h-12 w-12 mx-auto mb-2 text-gray-300" />
              <p>No emojis found for "{searchQuery}"</p>
            </div>
          )}
        </div>
      </DialogContent>
    </Dialog>
  )
}
